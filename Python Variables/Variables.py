#Creating Variables
x = 10;
y = "Mukter";
print(x);
print(y);
#Casting
x = str(10); #x will be '10'
y = int(10); # y will be 10
z = float(10); # z will be 10.0
print(x);
print(y);
print(z);
# Get the Type
age = 24;
y = "Mukto"
print(type(age));
print(type(y));
# Single or Double Quotes
x = "mukter";
# is the same as
y = 'mukter';
print(x);
print(y);
# Case Sensitive
a = 7;
A = "muk";
print(a);
# A will not overwrite a
print(A);